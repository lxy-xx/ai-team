export function renderDashboardI18nBootstrap() {
  const catalogJson = JSON.stringify(DASHBOARD_I18N).replaceAll("<", "\\u003c");
  return `    const DASHBOARD_I18N = ${catalogJson};
    const SUPPORTED_LOCALES = ["zh", "en"];

    function normalizeLocale(value) {
      const raw = String(value || "").toLowerCase();
      if (raw.startsWith("zh")) return "zh";
      if (raw.startsWith("en")) return "en";
      return "en";
    }

    function initialLocale() {
      const requested = bootParams.get("lang");
      const locale = normalizeLocale(requested || localStorage.getItem("aiTeamLocale") || navigator.language || "en");
      if (requested) localStorage.setItem("aiTeamLocale", locale);
      return locale;
    }

    state.locale = initialLocale();

    function localeTag() {
      return state.locale === "zh" ? "zh-CN" : "en-US";
    }

    function t(key, values = {}) {
      const catalog = DASHBOARD_I18N[state.locale] || DASHBOARD_I18N.en;
      const template = catalog[key] ?? DASHBOARD_I18N.en[key] ?? key;
      return String(template).replace(/\\{([a-zA-Z0-9_]+)\\}/g, (_, name) => values[name] ?? "");
    }

    function maybeT(key, fallback = "") {
      const value = t(key);
      return value === key ? String(fallback ?? "") : value;
    }

    function copy(value) {
      const text = String(value ?? "");
      if (!text) return "";
      const legacyOneOneLabel = ["1", "1"].join(":");
      const normalizedText = text.replaceAll(legacyOneOneLabel, "one one");
      return maybeT("copy." + normalizedText, normalizedText);
    }

    function actionLabel(value, fallback = "Run") {
      return maybeT("action." + value, copy(value || fallback));
    }

    function statusLabel(status) {
      return maybeT("status." + status, copy(status || "unknown"));
    }

    function navLabel(tab) {
      return maybeT("nav." + tab, copy(tab));
    }

    function filterLabel(filter) {
      return maybeT("filter." + filter, copy(filter));
    }

    function countText(key, count) {
      return t("count." + key, { count });
    }

    function readinessText(item, field) {
      return maybeT("readiness." + item.id + "." + field, item[field] || "");
    }

    function translateExactText(text) {
      const action = maybeT("action." + text, text);
      if (action !== text) return action;
      const copied = maybeT("copy." + text, text);
      if (copied !== text) return copied;
      return maybeT("status." + text, text);
    }

    function translatePlaceholder(text) {
      return maybeT("placeholder." + text, text);
    }

    function shouldSkipI18nNode(node) {
      const parent = node.parentElement;
      if (!parent) return true;
      return Boolean(parent.closest("script, style, pre, code, textarea, input, .mono, .card-title, .one-one-message > div, .trace-list, .readiness-result, .provider-status-output, .one-one-memory-output"));
    }

    function localizeDom(root = document.body) {
      if (!root) return;
      root.querySelectorAll("[placeholder]").forEach((element) => {
        const placeholder = element.getAttribute("placeholder");
        const translated = translatePlaceholder(placeholder);
        if (translated !== placeholder) element.setAttribute("placeholder", translated);
      });
      const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
      const nodes = [];
      while (walker.nextNode()) nodes.push(walker.currentNode);
      nodes.forEach((node) => {
        if (shouldSkipI18nNode(node)) return;
        const original = node.nodeValue || "";
        const trimmed = original.trim();
        if (!trimmed) return;
        const translated = translateExactText(trimmed);
        if (translated === trimmed) return;
        node.nodeValue = original.replace(trimmed, translated);
      });
    }

    function languageSwitchHtml() {
      return '<div class="language-switch" role="group" aria-label="' + escapeHtml(t("language.aria")) + '">' +
        '<button type="button" data-locale-option="zh">中文</button>' +
        '<button type="button" data-locale-option="en">EN</button>' +
      '</div>';
    }

    function renderLanguageSwitch() {
      const switchers = document.querySelectorAll(".language-switch");
      if (!switchers.length) return;
      switchers.forEach((switcher) => {
        switcher.setAttribute("aria-label", t("language.aria"));
        switcher.querySelectorAll("[data-locale-option]").forEach((button) => {
          const locale = normalizeLocale(button.dataset.localeOption);
          const active = state.locale === locale;
          button.classList.toggle("active", active);
          button.setAttribute("aria-pressed", active ? "true" : "false");
          button.onclick = () => {
            const nextLocale = normalizeLocale(button.dataset.localeOption);
            if (state.locale === nextLocale) return;
            state.locale = nextLocale;
            localStorage.setItem("aiTeamLocale", nextLocale);
            renderForLocaleSwitch();
          };
        });
      });
      document.documentElement.lang = localeTag();
      document.title = t("page.title");
    }

    function snapshotTransientForms() {
      return [".agent-config-card", ".provider-editor-card", ".one-one-modal", ".context-request-panel"].map((selector) => {
        const root = document.querySelector(selector);
        if (!root) return undefined;
        return {
          selector,
          fields: [...root.querySelectorAll("input, textarea, select")].map((field, index) => ({
            index,
            value: field.value,
            checked: field.checked,
            type: field.type
          }))
        };
      }).filter(Boolean);
    }

    function restoreTransientForms(snapshots = []) {
      snapshots.forEach((snapshot) => {
        const root = document.querySelector(snapshot.selector);
        if (!root) return;
        const fields = [...root.querySelectorAll("input, textarea, select")];
        snapshot.fields.forEach((saved) => {
          const field = fields[saved.index];
          if (!field) return;
          if (saved.type === "checkbox" || saved.type === "radio") field.checked = saved.checked;
          else field.value = saved.value;
        });
      });
    }

    function renderForLocaleSwitch() {
      const snapshots = snapshotTransientForms();
      render();
      restoreTransientForms(snapshots);
    }

`;
}
